"""Config template generator for SecondSight (config-unification task-4).

Provides three public functions:

  generate_config_template() -> str
      Returns the complete config.toml content with comments. The template is
      the single source of truth for default values displayed to operators.

  get_template_keys() -> set[str]
      Returns the set of dotted key paths present in the template
      (e.g. ``{"retention.raw_traces_ttl_days", "analysis.default_agent", ...}``).
      Used internally by diff_against_template().

  diff_against_template(existing_path: Path) -> list[str]
      Returns key paths that are in the template but absent from the existing
      config file. Empty list means the existing config is up-to-date.

  write_config_if_needed(home: Path, *, dry_run: bool = False) -> str
      The main entry point for ``secondsight init``. Implements the four-case
      protection logic:
        1. File absent  → create it, return "generated" message.
        2. File exists, malformed TOML → return "malformed" error message, no write.
        3. File exists, all template keys present → return "up-to-date" message.
        4. File exists, new template keys missing → return diff message, no write.

Import rules (MUST NOT be violated):
    - This module must NOT import from secondsight.analysis or secondsight.sdk.
    - This module MAY import from Python stdlib only (pathlib, sys, textwrap, etc.).

Assumption documented in scar-report:
    Scar-1: diff_against_template() compares flat key-path sets. It does NOT
    detect changes to a key's TYPE (e.g. string→array). Only presence/absence
    of a key path is compared. See scar-reports/task-4-scar.yaml.
"""

from __future__ import annotations

import sys
from pathlib import Path

# tomllib is stdlib in Python 3.11+; tomli is the backport for earlier versions.
if sys.version_info >= (3, 11):
    import tomllib
else:
    try:
        import tomllib
    except ImportError:
        import tomli as tomllib  # type: ignore[import,no-reuse-dep]

__all__ = [
    "generate_config_template",
    "get_template_keys",
    "diff_against_template",
    "write_config_if_needed",
    "MSG_GENERATED",
    "MSG_MALFORMED",
    "MSG_NEW_KEYS",
    "MSG_UP_TO_DATE",
]

# ---------------------------------------------------------------------------
# Message vocabulary constants
# ---------------------------------------------------------------------------
# These constants are the single source of truth for substrings that appear
# in write_config_if_needed() return values. cli/init.py._render_text()
# imports and uses them for pattern-matching, making the coupling explicit
# and rename-safe. If you change a constant here, the matching return string
# in write_config_if_needed() MUST be updated to keep the constant a substring
# of the new message (and vice versa). The death tests in
# tests/config/test_template.py::test_death_msg_constants_* enforce this.

MSG_GENERATED = "generated"
MSG_MALFORMED = "malformed"
MSG_NEW_KEYS = "new key"
MSG_UP_TO_DATE = "up-to-date"

