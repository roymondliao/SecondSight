def generate_config_template() -> str:
    """Return the complete config.toml template string.

    The returned string is valid TOML and contains human-readable comments
    explaining each setting. Calling this function twice returns byte-identical
    strings (idempotent).

    Returns:
        Complete config.toml content as a string, ending with a newline.
    """
    return _TEMPLATE


# ---------------------------------------------------------------------------
# Key-path extraction
# ---------------------------------------------------------------------------


def _flatten_toml(data: dict, prefix: str = "") -> set[str]:
    """Recursively flatten a parsed TOML dict into a set of dotted key paths.

    Only leaf nodes (non-dict values) are included. Intermediate section
    names are used as path prefixes.

    Examples::
        {"retention": {"raw_traces_ttl_days": 90}} → {"retention.raw_traces_ttl_days"}
        {"analysis": {"models": {"claude_code": ""}}} → {"analysis.models.claude_code"}

    Known limitation (Scar-1): array values are included as a single key path
    regardless of their contents. Type changes (string → array) are NOT
    detected — only presence/absence of the key is compared.
    """
    result: set[str] = set()
    for key, value in data.items():
        full_key = f"{prefix}.{key}" if prefix else key
        if isinstance(value, dict):
            result.update(_flatten_toml(value, full_key))
        else:
            result.add(full_key)
    return result


def get_template_keys() -> set[str]:
    """Return the set of dotted key paths present in the config template.

    Example return value::
        {
            "retention.raw_traces_ttl_days",
            "retention.analysis_ttl_days",
            "retention.cleanup_after_analysis",
            "analysis.default_agent",
            "analysis.models.claude_code",
            "analysis.models.codex",
            "analysis.models.opencode",
            "analysis.models.fallback.fallback_models",
            "analysis.read_project_file.enabled",
            "analysis.read_project_file.size_cap_kb",
            "analysis.read_project_file.denylist",
        }

    This set is used by diff_against_template() to detect missing keys in
    an existing config file.
    """
    parsed = tomllib.loads(_TEMPLATE)
    return _flatten_toml(parsed)


# ---------------------------------------------------------------------------
# Diff
# ---------------------------------------------------------------------------


def diff_against_template(existing_path: Path) -> list[str]:
    """Return key paths that are in the template but absent in ``existing_path``.

    Args:
        existing_path: Path to an existing, parseable config.toml. Caller is
            responsible for ensuring the file exists and is valid TOML before
            calling this function. Raises TOMLDecodeError if content is invalid.

    Returns:
        Sorted list of dotted key paths that the template defines but the
        existing file does not contain. An empty list means the existing file
        has all template keys (up-to-date).

    Known limitation (Scar-1):
        Key presence is compared by path only. A key that exists in both
        template and existing file but has a different type (e.g. string in
        template, array in existing) will NOT appear in the diff — only absent
        keys are reported.
    """
    with existing_path.open("rb") as fh:
        existing_data = tomllib.load(fh)

    template_keys = get_template_keys()
    existing_keys = _flatten_toml(existing_data)

    missing = template_keys - existing_keys
    return sorted(missing)


# ---------------------------------------------------------------------------
# Main entry point: write_config_if_needed
# ---------------------------------------------------------------------------


def write_config_if_needed(home: Path, *, dry_run: bool = False) -> str:
    """Write ~/.secondsight/config.toml if needed, with diff protection.

    Four-case logic:
        1. config.toml absent → create it (or report what would happen in dry_run).
        2. config.toml exists but malformed → return error message, do NOT write.
        3. config.toml exists, all template keys present → return up-to-date message.
        4. config.toml exists, new template keys missing → return diff message, no write.

    Args:
        home: The SecondSight home directory (e.g. ``Path.home() / ".secondsight"``).
            The function creates this directory if it does not exist (case 1 only).
        dry_run: If True, skip all writes; still compute what would happen and
            return a "would generate" message in case 1.

    Returns:
        A human-readable status message describing what was (or would be) done.
        Never raises SecondSightConfigError or TOMLDecodeError — all parse failures
        are caught and returned as an error message string so the caller can print
        it and continue (exit 0 per spec).
    """
    config_path = home / "config.toml"

    # ----- Case 1: file absent -----
    if not config_path.exists():
        if dry_run:
            # MSG_GENERATED is a substring of this message (case-insensitive).
            return f"[dry-run] would {MSG_GENERATED} {config_path} with default settings."
        home.mkdir(parents=True, exist_ok=True)
        config_path.write_text(generate_config_template(), encoding="utf-8")
        # MSG_GENERATED is a substring of this message (case-insensitive).
        return f"{MSG_GENERATED.capitalize()} {config_path} with default settings."

    # ----- File exists: attempt parse -----
    # We catch the broadest exception class here intentionally:
    # "never raise" is the contract for write_config_if_needed() so that
    # hook installation (stage 1-2) succeeds even if the config file is
    # broken. The tradeoff (sfc-2 in scar report): an I/O error (OSError)
    # would be misclassified as "malformed TOML". This is an accepted risk
    # — a malformed-TOML message is still actionable (operator checks the
    # file and discovers the I/O error), while a raised exception would
    # abort the entire init command.
    parseable = True
    try:
        with config_path.open("rb") as fh:
            tomllib.load(fh)
    except Exception:
        parseable = False

    if not parseable:
        # Case 2: malformed TOML (or I/O error — see comment above).
        # MSG_MALFORMED is a substring of this message (case-insensitive).
        return (
            f"{config_path} exists but is {MSG_MALFORMED}. "
            f"Run `secondsight config validate` for details."
        )

    # ----- Case 3 / 4: check diff -----
    # File is parseable. diff_against_template re-parses to extract key paths.
    missing_keys = diff_against_template(config_path)

    if not missing_keys:
        # Case 3: up-to-date. MSG_UP_TO_DATE is a substring of this message.
        return f"{config_path} is already {MSG_UP_TO_DATE}."

    # Case 4: new keys available — show diff, do NOT write.
    # MSG_NEW_KEYS is a substring of this message (case-insensitive).
    n = len(missing_keys)
    lines = [
        f"{config_path} has {n} {MSG_NEW_KEYS}{'s' if n != 1 else ''} available. Merge manually.",
        "New keys:",
    ]
    for key in missing_keys:
        lines.append(f"  + {key}")
    return "\n".join(lines)
